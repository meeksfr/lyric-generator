const reverseMap = {};
for (let propName in word2index) {
  let idx = word2index[propName];
  reverseMap[idx] = propName;
}
let model;
let output;

function generateText() {
  for (let k = 0; k < slider.value; k++) {
    let textArray = inputText.value.split(" ");
    let sequence = new Array(46);
    for (let i = 0; i < 46; i++) sequence[i] = 0;

    textArray.forEach((word) => {
      sequence.shift();
      sequence.push(word2index[word.toLowerCase()]);
    });
    const pred = model.predict(tf.tensor1d(sequence).expandDims());
    const index = pred.as1D().argMax().dataSync()[0];

    let nextWord = reverseMap[index];
    inputText.value = inputText.value.concat(" ", nextWord);
  }
  return inputText.value;
}

function changeNumTxt(e) {
  let numDisplay;
  if (e.target.value == 1) {
    numDisplay = "1 word";
  } else {
    numDisplay = String(e.target.value) + " words";
  }
  document.querySelector(".num-display").innerText = numDisplay;
}

function showSpinner() {
  return new Promise((resolve, reject) => {
    document.querySelector(".loading").classList.remove("hidden");
    document.querySelector(".lyrics").classList.add("hidden");
    setTimeout(() => {
      resolve();
    }, 1);
  });
}

function hideSpinner() {
  document.querySelector(".loading").classList.add("hidden");
  document.querySelector(".lyrics").classList.remove("hidden");
}
async function writeLyrics() {
  await showSpinner();
  output = generateText();
  document.querySelector(".text-input").value = "";
  document.querySelector(".lyrics").innerText = output;
  hideSpinner();
}

slider.addEventListener("input", function (e) {
  changeNumTxt(e);
});

